class UsersController < ApplicationController
  before_filter :ensure_authenticated_user, only: [:index]

  def me
    render json: current_user
  end
  # GET /users
  # GET /users.json
  def index
    @users = User.all

    render json: @users
  end

  # GET /users/1
  # GET /users/1.json
  def show
    @user = User.find(params[:id])

    render json: @user
  end

  # POST /users
  # POST /users.json
  def create
    @user = User.new(user_params)

    if @user.save
      render json: @user.session_api_key, status: :created, location: @user
      #cookies[:auth_token] = @user.session_api_key.access_token
      #redirect_to :root
    else
      render json: @user.errors, status: :unprocessable_entity
    end
  end

  # PATCH/PUT /users/1
  # PATCH/PUT /users/1.json
  def update
    @user = User.find(params[:id])

    if @user.update(user_params)
      head :no_content
    else
      render json: @user.errors, status: :unprocessable_entity
    end
  end

  # DELETE /users/1
  # DELETE /users/1.json
  def destroy
    @user = User.find(params[:id])
    @user.destroy

    head :no_content
  end

  def user_params
    params.require(:user).permit(
      :name,
      :email,
      :password
    )
  end
end
